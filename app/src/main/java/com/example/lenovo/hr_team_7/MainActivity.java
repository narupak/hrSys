    private void getServiceData() {
        Call<DataCollectionDao_pa> call = HttpManager_pa.getInstance().getService_pa().getDataList_pa();
        call.enqueue(new Callback<DataCollectionDao_pa>() {
            @Override
            public void onResponse(Call<DataCollectionDao_pa> call, Response<DataCollectionDao_pa> response) {
                if (response.isSuccessful()) {
                    DataCollectionDao_pa data = response.body();

                    Log.d("getServiceData", "if ::" + data. getProfile().get(0).getTio_id());

                    DataAdapter_pa dataAdapter = new DataAdapter_pa(data);
                    rvListPhoto.setAdapter(dataAdapter);
                } else {
                    Log.d("getServiceData", "else ::" + response.message());
                }
            }

            @Override
            public void onFailure(Call<DataCollectionDao_pa> call, Throwable t) {
                Log.d("getServiceData", "error ::" + t);
            }
        });
    }

